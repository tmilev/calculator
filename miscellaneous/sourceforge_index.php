<h2>
Computations with simple Lie algebras
</h2>
<p>   
The program has functions working with semisimple Lie algebras.
For example it computes html tables with the root subsystems of simple root systems of rank<=8.
Root subsystems parametrize the reductive root subalgebras of a simple Lie algebra 
(a root subalgebra is defined as a subalgebra containing a Cartan subalgebra). 
The calculator also includes tables with one representative of each conjugacy class of 
complex sl(2)-subalgebras of a simple Lie algebra, 
computed according to the algorithm described in Dynkin's paper 
"Semisimple Lie subalgebras of semisimple Lie algebras".
</p>
